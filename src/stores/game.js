// Pinia — официальный менеджер состояния для Vue 3.
// Состояние (state) — это данные, которые должны быть доступны в разных компонентах.
// defineStore — создаёт хранилище (store).
// Первый аргумент — уникальное имя стора ('game').
import { defineStore } from 'pinia'

export const useGameStore = defineStore('game', {
  // state — здесь хранятся данные стора (как data() в компоненте)
  state: () => ({
    // Размер одной ячейки сетки в пикселях.
    // Подбирается вручную под конкретную карту, поэтому хранится в сторе —
    // в будущем его можно будет менять из панели настроек.
    cellSize: 60,
    colorGrid: 'rgba(0,0,0,0.3)',
  }),

  // getters — вычисляемые значения на основе state (как computed в компоненте)
  getters: {},

  // actions — методы для изменения state (как methods в компоненте)
  actions: {},
})
